oncube(a,b).

/*----------------------------------------------------------------------------------------------------------------------------------*/
/* Actions 																															*/
/*----------------------------------------------------------------------------------------------------------------------------------*/
/* 1st action pickup cube X from table 																								*/
/* and leave it on other cube Y																										*/
/*----------------------------------------------------------------------------------------------------------------------------------*/

puton(X,Y) :- 
	/* preconditions */
	ontable(X),
	freecube(Y),
	/* effects */
	assert(oncube(X,Y)),
	retract(freecube(Y)),
	retract(ontable(X)).

/*----------------------------------------------------------------------------------------------------------------------------------*/
/* 2nd action putdown cube X which is 																								*/
/* on Y and put it on table																											*/
/*----------------------------------------------------------------------------------------------------------------------------------*/
putdown(X) :- 
	/* preconditions */
	oncube(X,Y),
	freecube(X),
	/* effects */
	assert(ontable(X)),
	assert(freecube(Y)),
	retract(oncube(X,Y)).

